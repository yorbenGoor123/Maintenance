    {
        try
        {
            $userName = $request->get('name');
            $userEmail = $request->get('email');
            $userPassword = $request->get('password');

            User::create([
                'name'  =>  $userName,
                'email' =>  $userEmail,
                'password' => $userPassword
            ]);

            return response()->json([
                'name'   =>  $userName,
                'email' =>  $userEmail,
                'password' => $userPassword
            ]);
        }
        catch(Exception $e)
        {
            Log::error($e);
        }
    }

